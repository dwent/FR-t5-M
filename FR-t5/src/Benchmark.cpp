void Read_LindahlPair_Info(const string file, string ID, vector<string> &info)
{
	string sbuf;
	int i=0;
	bool j=false;
	ifstream fin(file.c_str());
	if(!fin)
	{
		cout<<"erro........"<<file<<endl;
		exit(-1);
	}
	while(!fin.eof())
	{
		sbuf.clear();
		getline(fin,sbuf);
		if(!sbuf.empty() && sbuf.compare(0,2,"  ",2)!=0 && sbuf==ID)
		{
			j=true;
			break;
		}
	}
	if(j)
		while(!fin.eof())
		{
			sbuf.clear();
			getline(fin,sbuf);
			if(sbuf.compare(0,8,"        ",8)==0)
				info.push_back(sbuf.substr(8));
			else
				break;
		}
	else
		cout<<"ID is not in the 976 proteins."<<endl;
	fin.close();
}
